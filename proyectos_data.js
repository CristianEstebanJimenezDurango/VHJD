// Función para renderizar los proyectos
function renderProjects() {
    const pubGrid = document.querySelector('#publicaciones .projects-grid');
    const invGrid = document.querySelector('#investigaciones .projects-grid');

    if (pubGrid) {
        pubGrid.innerHTML = '';
        proyectosData.publicaciones.forEach(p => {
            pubGrid.innerHTML += createProjectCardContent(p);
        });
    }

    if (invGrid) {
        invGrid.innerHTML = '';
        proyectosData.investigaciones.forEach(p => {
            invGrid.innerHTML += createProjectCardContent(p);
        });
    }
}

function createProjectCardContent(p) {
    const args = [
        p.modalTitle, p.modalAbstract, p.modalResults,
        p.linkUrl, p.instagramUrl, p.twitterUrl, p.downloadUrl
    ].map(arg => arg ? '`' + arg + '`' : 'null').join(', ');

    return `
        <div class="project-card" style="background-image: url('${p.bgImage}');" 
             onclick="openProjectModal(${args})">
             <div class="project-overlay">
                 <h3>${p.cardTitle}</h3>
                 <p>${p.cardText}</p>
             </div>
        </div>
    `;
}

document.addEventListener('DOMContentLoaded', () => {
    if (document.querySelector('.projects-grid')) {
        renderProjects();
    }
});
